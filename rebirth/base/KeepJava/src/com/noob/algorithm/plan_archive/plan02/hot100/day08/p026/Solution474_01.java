        // 3.åˆå§‹åŒ–
        dp[0][0] = 0; // åŽç»­ç»ç”±é€’æŽ¨æŽ¨å¯¼

        // 4.dp æž„å»º(ä¸€ç»´æ•°ç»„ï¼šå…ˆç‰©å“åŽèƒŒåŒ…ã€èƒŒåŒ…é€†åº)
        // å¤–å±‚ç‰©å“ï¼šstr
